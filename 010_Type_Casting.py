#Type casting in python is done through constructor function
#int type casting
x = int(1)
y = int(4.5)
z = int("6")

print(x, y, z)

#float type casting

x1 = float(1)
y1 = float(2.6)
y2 = float("2.8")
y3 = float("1")

print(x1, y1, y2, y3)


#string type casting

x2 = str("Hello")
x3 = str(3)
x4 = str(4.4)

print(x2, x3, x4)
