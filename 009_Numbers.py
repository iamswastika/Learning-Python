#Numeric Type in Python
#int
#float
#complex

x = 1
y = -34
z = 23.6
s = -63.6678
r = 1j

print(x)
print(y)
print(z)
print(type(s))
print(type(r))

#Float can also be scientific numbers with an "e" to indicate the power of 10.

a = 35e6
b = 44E2
c = -456e100
print(a)
print(b)
print(type(c))

#Complex numbers are written with a "j" as the imaginary part

d = 1+2j
e = 1.4j
f = -44.6j+1
g = 6j
print(d,e,f)
print(type(g))
